% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
Fini= str2double(get(handles.edit1,'String'))
Ffin= str2double(get(handles.edit2,'String'))
Nfreq= str2double(get(handles.edit3,'String'))
ZeroP= 1
Ers= str2double(get(handles.edit7,'String'))
Rad= str2double(get(handles.edit6,'String'))
Sloc= str2double(get(handles.edit4,'String'))
Yap= str2double(get(handles.edit15,'String'))
Yloc=  str2double(get(handles.edit10,'String'))
Xn= str2num(get(handles.edit8,'String'))
Zn= str2num(get(handles.edit9,'String'))
Yn= str2num(get(handles.edit12,'String'))
En= 1
Er=ones(1,Nfreq)*Ers;
[BSCAN,f,dd,Mu,dR,theta]=cylscan(Fini,Ffin,Nfreq,ZeroP,Er,Rad,Sloc,Yap,Yloc,Xn,Zn,Yn,En);
handles.output=BSCAN;
[filename, pathname] = uiputfile( {'*.mat';'*.*'}, 'Save as');
    % If 'Cancel' was selected then return
    if isequal([filename,pathname],[0,0])
        return
    else
        % Construct the full path and save
        File = fullfile(pathname,filename);
        save(File,'BSCAN', 'f', 'dd', 'Mu', 'dR', 'theta');
        handles.File=File;
        guidata(hObject, handles)
    end
